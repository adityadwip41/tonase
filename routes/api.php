Route::middleware('auth:api')->get('/user', function (Request $request) {
    return $request->user();
});

Route::group([
    'middleware' => 'api',
    'prefix' => 'auth'
], function ($router) {
    Route::post('login', 'AuthController@login');
    Route::post('register', 'AuthController@register');
    Route::post('logout', 'AuthController@logout');
    Route::post('refresh', 'AuthController@refresh');
    Route::get('user-profile', 'AuthController@userProfile');
});

Route::post('/topup/{rek}', 'TransactionController@topup');
Route::post('/rekening', 'TransactionController@rekening');
Route::post('/withdraw/{rek}', 'TransactionController@withdraw');
Route::post('/transfer/{rek}', 'TransactionController@transfer');
